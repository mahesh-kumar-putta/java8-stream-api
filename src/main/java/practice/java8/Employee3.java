package practice.java8;

public class Employee3 {

	private int salary;
	
	private String dept;

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public Employee3(int salary, String dept) {
		super();
		this.salary = salary;
		this.dept = dept;
	}
	
	
	
}
